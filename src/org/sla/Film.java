package org.sla;

public class Film {
    // Fields
    int rank;
    String title;
    int releaseYear;
    long gross;

    public Film(int rank, String title, int releaseYear, long gross) {
        this.rank = rank;
        this.title = title;
        this.releaseYear = releaseYear;
        this.gross = gross;
    }
}
